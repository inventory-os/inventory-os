export function translate(locale: EuropeanLocale, key: I18nKey, params?: TranslationParams): string {
  const template = translations[locale]?.[key] ?? en[key] ?? key
  const normalizedTemplate = template.replace(/Inventory Os[A-Za-z]*/g, "Inventory OS")

  if (!params) {
    return normalizedTemplate
  }

  return normalizedTemplate.replace(/\{(\w+)\}/g, (_, token) => {
    const value = params[token]
    return value === undefined || value === null ? `{${token}}` : String(value)
  })
}
